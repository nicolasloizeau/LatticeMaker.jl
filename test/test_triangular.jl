
using LatticeMaker
using Test
include("basic_tests.jl")


@testset "TriangularLattice" begin
    Nx = 4
    Ny = 5
    lattice = TriangularLattice(Nx, Ny)
    basic_tests(lattice, (Nx, Ny))
    @test length(edges(lattice)) == (Nx-1)*Ny + ((Nx-1)*2+1)*(Ny-1)
    e1 = Set(edges(lattice))
    e2 = Set([[1, 6], [1, 2], [2, 7], [2, 3], [3, 8], [3, 4], [4, 9], [4, 5], [5, 10], [6, 11], [6, 7], [2, 6], [7, 12], [7, 8], [3, 7], [8, 13], [8, 9], [4, 8], [9, 14], [9, 10], [5, 9], [10, 15], [11, 16], [11, 12], [7, 11], [12, 17], [12, 13], [8, 12], [13, 18], [13, 14], [9, 13], [14, 19], [14, 15], [10, 14], [15, 20], [16, 17], [12, 16], [17, 18], [13, 17], [18, 19], [14, 18], [19, 20], [15, 19]])
    @test e1 == e2
    lattice = TriangularLattice(Nx, Ny; periodic=(true, true))
    basic_tests(lattice, (Nx, Ny))
    @test length(edges(lattice)) == Nx*Ny*3
    e1 = Set(edges(lattice))
    e2 = Set([[1, 6], [1, 2], [1, 17], [2, 7], [2, 3], [2, 18], [3, 8], [3, 4], [3, 19], [4, 9], [4, 5], [4, 20], [5, 10], [1, 5], [5, 16], [6, 11], [6, 7], [2, 6], [7, 12], [7, 8], [3, 7], [8, 13], [8, 9], [4, 8], [9, 14], [9, 10], [5, 9], [10, 15], [6, 10], [1, 10], [11, 16], [11, 12], [7, 11], [12, 17], [12, 13], [8, 12], [13, 18], [13, 14], [9, 13], [14, 19], [14, 15], [10, 14], [15, 20], [11, 15], [6, 15], [1, 16], [16, 17], [12, 16], [2, 17], [17, 18], [13, 17], [3, 18], [18, 19], [14, 18], [4, 19], [19, 20], [15, 19], [5, 20], [16, 20], [11, 20]])
    @test e1 == e2
end
